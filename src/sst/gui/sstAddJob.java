                                + "values('"+alldate+"','"+servtyp+"','"+serviceSlot+"','"+veh+"','"+vehicletyp+"','"+id+"')";
                        
                        DB.getDbCon().insert(sql);
                        //ResultSet rs=DB.getDbCon().query(sql1);
                        
                        JOptionPane.showMessageDialog(null,"Booking successfull");
                        
                        vehno.setText("");
